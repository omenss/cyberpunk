package learning.mergeTwoLIstNode;

/**
 * @author lujun
 * @date 2023/7/31 10:36
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
